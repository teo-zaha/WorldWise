package com.worldwise.backend;

public class Quiz {
    private String name;
    private String prompt;
    private String answer;
    private String file;

    public String getName() {
        return name;
    }
    public String getPrompt() {
        return prompt;
    }
    public String getAnswer() {
        return answer;
    }
    public String getFile() {
        return file;
    }
}
